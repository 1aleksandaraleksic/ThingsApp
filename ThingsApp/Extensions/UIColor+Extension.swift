import UIKit

extension UIColor {

    static func primaryBlue() -> UIColor {
        UIColor(red: 0.316, green: 0.7, blue: 0.788, alpha: 1)
    }

    static func primaryGreen() -> UIColor {
        UIColor(red: 0.734, green: 0.869, blue: 0.314, alpha: 1)
    }
}
